public class Operadore {
    public static void main(String[] args) throws Exception {

        String concatenação = "?";

        concatenação = 1+1+1+"1";
        
        System.out.println(concatenação);

        concatenação = 1+"1"+1+1;

        System.out.println(concatenação);

        concatenação = 1+"1"+1+"1";

        System.out.println(concatenação);

        concatenação = "1"+1+1+1;

        System.out.println(concatenação);

        concatenação = "1"+(1+1+1);

        System.out.println(concatenação);



    }    
}
